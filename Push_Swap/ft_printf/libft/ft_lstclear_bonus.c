#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*list;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		ft_lstdelone(*lst, del);
		list = *lst;
		*lst = list->next;
	}
}
